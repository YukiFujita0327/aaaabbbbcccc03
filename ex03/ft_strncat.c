#include <stdio.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int				n;
	unsigned int	i;

	n = 0;
	i = 0;
	while (dest[n] != '\0')
	{
		n++;
	}
	while (i < nb && src[i] != '\0')
	{
		dest[n] = src[i];
		n++;
		i++;
	}
	dest[n] = '\0';
	return (dest);
}
